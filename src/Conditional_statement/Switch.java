package Conditional_statement;

public class Switch {
	public static void main(String[] args) {
		switch(4) {
		case 1:{
			System.out.println("wendseday");
			break;
		}
		case 2:{
			System.out.println("monday");
			break;
		}
		case 3:{
			System.out.println("tuseday");
			break;
		}
		default:
			System.out.println("holiday");
		
		
		}
	}

}
